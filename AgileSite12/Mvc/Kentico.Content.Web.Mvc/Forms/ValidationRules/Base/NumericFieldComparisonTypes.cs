namespace Kentico.Forms.Web.Mvc
{
    /// <summary>
    /// Possible numeric comparison types used in form field validation.
    /// </summary>
    public enum NumericFieldComparisonTypes
    {
        /// <summary>
        /// Less than.
        /// </summary>
        LessThan,

        /// <summary>
        /// Less than or equal.
        /// </summary>
        LessThanOrEqual,

        /// <summary>
        /// Greated than.
        /// </summary>
        GreaterThan,

        /// <summary>
        /// Greater than or equal.
        /// </summary>
        GreaterThanOrEqual,

        /// <summary>
        /// Equal.
        /// </summary>
        Equal,

        /// <summary>
        /// Not equal.
        /// </summary>
        NotEqual
    }
}
